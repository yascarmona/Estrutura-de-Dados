public class Funcionario {
    private String Nome;
    private int NumberRH;
    private String Setor;

    public String getNome() {
        return Nome;
    }

    public void setNome(String nome) {
        Nome = nome;
    }

    public int getNumberRH() {
        return NumberRH;
    }

    public void setNumberRH(int numberRH) {
        NumberRH = numberRH;
    }

    public String getSetor() {
        return Setor;
    }

    public void setSetor(String setor) {
        Setor = setor;
    }

}
